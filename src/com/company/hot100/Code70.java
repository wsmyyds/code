    public static int climbStairs(int n) {

        if (n<=2){
            if (n==1) {
                return 1;
            }else{
                return 2;
            }
        }
        int res = 0;
        int pre1 = 2,pre2 = 1;
        int i = 2;
        while(i<n){

            res = pre1 + pre2;
            pre2 = pre1;
            pre1 = res;
            i++;
        }
        return res;
    }
